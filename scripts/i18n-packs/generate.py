PLACEHOLDERS = (
    "{{count}}",
    "{{pct}}",
    "{{email}}",
    "{{when}}",
    "{{date}}",
    "{{name}}",
    "{{task}}",
    "{{topic}}",
    "{{mastered}}",
    "{{total}}",
    "{{price}}",
    "{{day}}",
    "{{type}}",
    "{{calendar}}",
)


def merge_lang(lang: str, en: dict[str, str]) -> dict[str, str]:
    partial = json.loads((PARTIAL_DIR / f"{lang}.json").read_text())
    new = NEW[lang]
    out: dict[str, str] = {}
    for key in en:
        if key in partial:
            out[key] = partial[key]
        elif key in new:
            out[key] = new[key]
        else:
            raise KeyError(f"Missing translation for {lang}.{key}")
    return dict(sorted(out.items()))


def validate(en: dict[str, str], translated: dict[str, str], lang: str) -> None:
    assert set(translated) == set(en), f"{lang}: key mismatch"
    for key, en_val in en.items():
        tr_val = translated[key]
        for ph in PLACEHOLDERS:
            if ph in en_val and ph not in tr_val:
                raise ValueError(f"{lang}.{key}: missing placeholder {ph}")
        if "\\n" in en_val and "\\n" not in tr_val:
            raise ValueError(f"{lang}.{key}: missing \\n")
        if key == "app.name" and tr_val != "Recall":
            raise ValueError(f"{lang}.app.name must stay Recall")


def main() -> None:
    en = json.loads(EN_PATH.read_text())
    # Regenerate from inline translations.
    for lang in ("es", "fr", "de", "it"):
        pack = merge_lang(lang, en)
        validate(en, pack, lang)
        out_path = OUT_DIR / f"{lang}.json"
        out_path.write_text(json.dumps(pack, ensure_ascii=False, indent=2) + "\n")
        print(f"{lang}.json: {len(pack)} keys")
    # Validate existing packs for locales without inline translations.
    for lang in ("pt", "ru", "tr", "am"):
        pack_path = OUT_DIR / f"{lang}.json"
        if pack_path.exists():
            pack = json.loads(pack_path.read_text())
            validate(en, pack, lang)
            print(f"{lang}.json: validated {len(pack)} keys (existing pack)")


if __name__ == "__main__":
    main()
